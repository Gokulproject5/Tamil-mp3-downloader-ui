      function showAlbums(categoryId, categoryName) {
        document.getElementById("categoriesSection").classList.add("d-none");
        document.getElementById("albumsSection").classList.remove("d-none");
        document.getElementById("albumsTitle").textContent =
          categoryName + " Albums";

        const albums = albumsData[categoryId] || [];
        const grid = document.getElementById("albumsGrid");
        grid.innerHTML = "";

        albums.forEach((album) => {
          grid.innerHTML += `
                    <div class="col-lg-4 col-md-6">
                        <div class="album-card">
                            <div class="album-cover"><i class="fas fa-compact-disc"></i></div>
                            <h5 class="fw-bold mb-2">${album.title}</h5>
                            <p class=" mb-3"><i class="fas fa-user me-2"></i>${album.artist}</p>
                          
                        </div>
                    </div>
                `;
        });
      }

      function hideAlbums(e) {
        e.preventDefault();
        document.getElementById("albumsSection").classList.add("d-none");
        document.getElementById("categoriesSection").classList.remove("d-none");
      }


document.addEventListener('DOMContentLoaded', function () {
  const toggler = document.querySelector('.navbar-toggler');
  const navCollapse = document.getElementById('navbarNav');

  if (toggler) {
    toggler.addEventListener('click', function () {
      this.classList.toggle('open');
      const expanded = this.getAttribute('aria-expanded') === 'true';
      this.setAttribute('aria-expanded', (!expanded).toString());
    });
  }


  document.querySelectorAll('.navbar-nav .nav-link').forEach(function (link) {
    link.addEventListener('click', function () {
      if (navCollapse && navCollapse.classList.contains('show')) {
        navCollapse.classList.remove('show');
      }
     
      if (toggler && toggler.classList.contains('open')) toggler.classList.remove('open');
      if (toggler) toggler.setAttribute('aria-expanded', 'false');
    });
  });
});

